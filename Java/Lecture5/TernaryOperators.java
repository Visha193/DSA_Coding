public class TernaryOperators {
    public static void main(String[] args) {

        // Ternary Operator
        // 1. Replace if else Statement in certain Situations
        // 2. Evaluates the test condition and executes a block of code based on the result of the Condition
        // Syntax : Condition ? Exp1 : Exp2 ;
        
        int n = 75;

        String result = (n>70) ? "Pass": "Fail";

        System.out.println(result);

    }
}
