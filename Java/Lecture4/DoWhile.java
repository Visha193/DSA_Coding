package Lecture4;

public class DoWhile {
    public static void main(String[] args) {
        
        int n2 = 10;
        int i2 = 1;

        do{
            System.out.println("Hello" + i2);
            i2++;
        }while(i2 <= n2);

        int m = 10;
        do{
            System.out.println("Hello");
            m++;
        }while(m <= 9);

    }
}
